public class Laskin {
    public static int addme(int a, int b) {
        return a + b;
    }
    public static int subme(int a, int b) {
        return a - b;
    }
    public static int mulme(int a, int b) {
        return a * b;
    }
    public static int divme(int a, int b) {
        return a / b;
    }
    public static void main(String[] args) {
        System.out.println(addme(10, 20));
        System.out.println(subme(10, 20));
        System.out.println(mulme(10, 20));
        System.out.println(divme(10, 20));
    }
}
